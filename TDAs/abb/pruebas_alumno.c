void pruebas_iter_interno(){

    printf("%s\n", "PRUEBAS ITER INTERNO");

    abb_t* abb = abb_crear(strcmp,NULL);

    char* claves[5] = {"4", "5", "3","1","2"};
    int valores[5] = {4, 5, 3, 1, 2};
    int suma = 0;

    abb_in_order(abb, sumar_todos, &suma);
    print_test("Se trato de iterar internamente una abb vacio", true);

    for( int i=0;i<5;i++ ) abb_guardar(abb, claves[i], &valores[i]);
    
    abb_in_order(abb, sumar_todos, &suma);
    print_test("Se itero internamente un abb utilizando el parametro extra.",suma==15);

    suma=0;

    abb_in_order(abb, corte_rama_izquierda, &suma);
    print_test("Se itero internamente un abb con utilizando una condicion de corte.",suma==3);

    abb_destruir(abb);
}

